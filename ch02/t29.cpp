//프로그램2- 문자 변수와 리터럴 사용하기
/* char자료형의 변수를 선언하고 초기화하는 프로그램*/
#include <iostream>
using namespace std;
int main()
{
    char first = 'A';
    char second = 65;
    char third = 'B';
    char fourth = 66;

    cout << "first의 값 : " << first << endl;
    cout << "second의 값 : " << second << endl;
    cout << "third의 값 : " << third << endl;
    cout << "fourth의 값 : " << fourth << endl;
    return 0;
}